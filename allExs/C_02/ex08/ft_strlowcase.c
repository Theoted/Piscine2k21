char	*ft_strlowcase(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 65;
	while (str[i])
	{
		while (j < 91)
		{
			if (str[i] == j)
				str[i] += 32;
			j++;
		}
		j = 65;
		i++;
	}
	return (str);
}
