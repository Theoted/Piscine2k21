char	*ft_strupcase(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 97;
	while (str[i])
	{
		while (j < 123)
		{
			if (str[i] == j)
				str[i] -= 32;
			j++;
		}
		i++;
		j = 97;
	}
	return (str);
}
