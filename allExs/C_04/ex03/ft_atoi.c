int	ft_atoi(char *str)
{
	int	i;
	int	neg;
	int	num;

	neg = 1;
	num = 0;
	i = 0;
	while (str[i] < 33)
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg *= -1;
		i++;
	}
	while (str[i] > 47 && str[i] < 58)
	{
		num = (num * 10) + (str[i] - 48);
		i++;
	}
	return (num * neg);
}
